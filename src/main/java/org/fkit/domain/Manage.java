package org.fkit.domain;


public class Manage {
	private String kind;
	private String url;
	private String content;
	private String two_url;
	private String one_url;
	private String thr_url;
	private String for_url;
	private String head;
	private String path;
	
	public Manage() {
			super();}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getTwo_url() {
		return two_url;
	}

	public void setTwo_url(String two_url) {
		this.two_url = two_url;
	}

	public String getOne_url() {
		return one_url;
	}

	public void setOne_url(String one_url) {
		this.one_url = one_url;
	}

	public String getThr_url() {
		return thr_url;
	}

	public void setThr_url(String thr_url) {
		this.thr_url = thr_url;
	}

	public String getHead() {
		return head;
	}

	public void setHead(String head) {
		this.head = head;
	}



	public String getKind() {
		return kind;
	}



	public void setKind(String kind) {
		this.kind = kind;
	}

	public String getFor_url() {
		return for_url;
	}

	public void setFor_url(String for_url) {
		this.for_url = for_url;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}
}
